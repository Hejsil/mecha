fn umatcher(comptime f: fn(c: u21) bool) Parser([]const u8) {
    return struct {
        const Res = Result([]const u8);
        fn func(str: []const u8) ?Res {
            if (str.len == 0)
                return null;

            const cp_len = unicode.utf8ByteSequenceLength(str[0]) catch return null;

            if (cp_len > str.len)
                return null;

            const cp = unicode.utf8Decode(str[0..cp_len]) catch return null;

            if (!f(cp))
                return null;

            return Res.init(str[0..cp_len], str[cp_len..]);
        }
    }.func;
}

fn blockMatcher(comptime b: Blocks) fn(cp: u21) bool {
    return struct {
        fn func(cp: u21) bool {
            return b == block(cp);
        }
    }.func;
}

pub const BasicLatin = umatcher(blockMatcher(.BasicLatin));

